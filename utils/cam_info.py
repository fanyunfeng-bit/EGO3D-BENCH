def estimate_rt(view):
    # Angle definitions in radians
    deg = np.pi / 180
    angles = {
        "right_45": -45 * deg,
        "right_90": -90 * deg,
        "right_135": -135 * deg,
        "right_180": -180 * deg,
        "left_45": 45 * deg,
        "left_90": 90 * deg,
        "left_135": 135 * deg,
    }

    # Rotation matrix around Y-axis
    def rot_y(theta):
        return np.array([
            [np.cos(theta), 0, -np.sin(theta)],
            [0,             1, 0            ],
            [np.sin(theta), 0,  np.cos(theta)]
        ])

    # Compute matrices
    # R = {name: rot_y(theta) for name, theta in angles.items()}
    R =  rot_y(angles[view])
    T =  np.array([0,0,0])
    
    return R 

